/*
 * Hardware independent key code for meta keys
 */

#ifndef _KEYCODE_H

/*
 * Ascii map
 */
#define K_BKSP		0x08
#define K_TAB		0x09
#define K_ENTR		0x0D
#define K_ESC		0x1b
#define K_DEL		0x7f

/*
 * Special key code
 */
#define K_CTRL		0x80
#define K_SHFT		0x81
#define K_ALT		0x82
#define K_CAPS		0x83

#define K_INS		0x84
#define K_HOME		0x85
#define K_END		0x86
#define K_PGUP		0x87
#define K_PGDN		0x88
#define K_UP		0x89
#define K_DOWN		0x8a
#define K_LEFT		0x8b
#define K_RGHT		0x8c

#define K_F1		0x90
#define K_F2		0x91
#define K_F3		0x92
#define K_F4		0x93
#define K_F5		0x94
#define K_F6		0x95
#define K_F7		0x96
#define K_F8		0x97
#define K_F9		0x98
#define K_F10		0x99
#define K_F11		0x9a
#define K_F12		0x9b

#endif /* _KEYCODE_H */
